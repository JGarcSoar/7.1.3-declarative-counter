const multiply = (a, b) => a * b;
const firstNum = 5;
const secondNum = 3;
let product = multiply(firstNum, secondNum);
console.log(`The product of ${firstNum} and ${secondNum} is ${product}`);

const division = (a, b) => a / b;
const thirdNun = 10000;
const fourthNum = 82;
let divisionProduct = division(thirdNun, fourthNum);
console.log(
  `The product of ${thirdNun} and ${fourthNum} is ${divisionProduct}`
);
