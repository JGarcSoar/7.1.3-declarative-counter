"use strict";

var multiply = function multiply(a, b) {
  return a * b;
};
var firstNum = 5;
var secondNum = 3;
var product = multiply(firstNum, secondNum);
console.log("The product of ".concat(firstNum, " and ").concat(secondNum, " is ").concat(product));
var division = function division(a, b) {
  return a / b;
};
var thirdNun = 10000;
var fourthNum = 82;
var divisionProduct = division(thirdNun, fourthNum);
console.log("The product of ".concat(thirdNun, " and ").concat(fourthNum, " is ").concat(divisionProduct));